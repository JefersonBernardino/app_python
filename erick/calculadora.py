# Aulas de variaveis.
valor1_str = input('digite o primeiro valor: ')
print("valor1: " + valor1_str)
valor2_str = input('digite o segundo valor: ')
print("valor2: " + valor2_str)

soma= valor1_str + valor2_str
print("resultado da str: " + soma)

valor1_int = int(valor1_str)
valor2_int = int(valor2_str)
soma = valor1_int + valor2_int
print("resultado do int: " + str(soma))

subtracao = valor1_int - valor2_int
print("resultado do int: " + str(subtracao))
